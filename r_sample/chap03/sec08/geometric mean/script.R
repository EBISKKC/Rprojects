# 3年間の収益率
rate <- c(
  1+(20/100),  # +20％
  1-(30/100),  # -30％
  1+(10/100)   # +10％
)
x <- prod(rate)^(1/length(rate))  # 収益率の幾何平均を求める
cat("平均収益率", x - 1)          # 出力



# 以下は練習問題
r <- c(
  1+(10/100),  # +20％
  1-(12/100),  # -12％
  1+(3/100),   # +3％
  1+(8/100),   # +8％
  1+(4/100)    # +4％
)
x1 <- prod(r)^(1/length(r))  # 収益率の幾何平均を求める
cat("平均収益率", x1 - 1)    # 出力
